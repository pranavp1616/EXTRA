    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
           if(ValidateSeatAvailality())
           {
               int seats = Integer.parseInt(Seats_TextField.getText());
           
            float fare = Float.parseFloat(Fare_TextField.getText());
            total_fare= seats * fare;
            Total_Fare_TextField.setText(total_fare + "");
               STATUS.setText("Seats available");
           }
           else
           {
               STATUS.setText("Seats not available");
           }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         if(ValidateSeatAvailality())
         {
            int seats = Integer.parseInt(Seats_TextField.getText());
            PayMentPage pPage = new PayMentPage(bFrame,busID,total_fare,seats);
            bFrame.remove(this);
            bFrame.add(pPage);
            bFrame.repaint();
            bFrame.revalidate();
            
               STATUS.setText("Seats available");
         }
         else
         {
             
               STATUS.setText("Seats not available");
         }
    }//GEN-LAST:event_jButton1ActionPerformed

