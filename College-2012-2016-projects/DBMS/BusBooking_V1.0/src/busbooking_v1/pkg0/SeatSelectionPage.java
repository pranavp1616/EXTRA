    public boolean ValidateSeatAvailality()
    {
        if(Integer.parseInt(Seats_Available_TextField.getText()) >= Integer.parseInt(Seats_TextField.getText()))
        {
            if(Integer.parseInt(Seats_TextField.getText()) <=5)
                return true;
            else
                return false;
        }
        else
            return false;
    }
    public void DisplayBusDetails()
    {
        Seats_TextField.setColumns(TextField_width);
        Seats_TextField.setText("1");
        
        Total_Fare_TextField.setColumns(TextField_width);
        Total_Fare_TextField.setText("0");
        
        SelectedBusName.setColumns(TextField_width);
        SelectedBusName.setText("");
        
        Seats_Available_TextField.setColumns(TextField_width);
        Seats_Available_TextField.setText("");
        
        Fare_TextField.setColumns(TextField_width);
        Fare_TextField.setText("");
                
        JDBC j_temp = new JDBC();
        
        Statement stmt = j_temp.getConn();
        String sql = "select bus_name,seats_booked,fare from bus where bus_id = '"+busID +"';";
        try
        {
            ResultSet rs = stmt.executeQuery(sql);
            rs.next();
            SelectedBusName.setText(rs.getString("bus_name").toString());
            int temp = 45 - Integer.parseInt(rs.getString("seats_booked"));
            Seats_Available_TextField.setText(temp + "");
            Fare_TextField.setText(rs.getString("fare").toString());
            rs.close();
            j_temp.CloseAll();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
